# Módilos con funciones
texto="Ejemplo de funciones"
#Función para calcular la resta de dos números
def resta(a,b):
    return a-b

#Función para dividir dos números
def div(a,b):
    return a/b

#Función para multiplicar dos números
def producto(a,b):
    return a*b

#Función para sacar el módulo de una división
def modulo(a,b):
    return a%b

#Función para sumar dos números
def suma(a,b):
    return a+b